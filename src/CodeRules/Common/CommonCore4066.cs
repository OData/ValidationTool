        /// <summary>
        /// Verify Common.Core.4066
        /// </summary>
        /// <param name="context">Service context</param>
        /// <param name="info">out parameter to return violation information when rule fail</param>
        /// <returns>true if rule passes; false otherwise</returns>
        public override bool? Verify(ServiceContext context, out ExtensionRuleViolationInfo info)
        {
            if (context == null)
            {
                throw new ArgumentNullException("context");
            }

            bool? passed = null;
            info = null;
            bool isJsonType = false;
            bool isAtomType = false;

            Uri absoluteUri = new Uri(context.Destination.OriginalString.Split('?')[0]);
            Uri relativeUri = new Uri("?$format=application/json", UriKind.Relative);
            Uri combinedUri = new Uri(absoluteUri, relativeUri);
            Response response = WebHelper.Get(combinedUri, null, RuleEngineSetting.Instance().DefaultMaximumPayloadSize, null);
            var payloadFormat = response.ResponsePayload.GetFormatFromPayload();

            if (payloadFormat == RuleEngine.PayloadFormat.JsonLight)
            {
                isJsonType = true;
            }

            relativeUri = new Uri("?$format=application/atom+xml", UriKind.Relative);
            combinedUri = new Uri(absoluteUri, relativeUri);
            response = WebHelper.Get(combinedUri, null, RuleEngineSetting.Instance().DefaultMaximumPayloadSize, null);
            payloadFormat = response.ResponsePayload.GetFormatFromPayload();

            if (payloadFormat == RuleEngine.PayloadFormat.Atom || payloadFormat == RuleEngine.PayloadFormat.Xml)
            {
                isAtomType = true;
            }

            if (isJsonType || isAtomType)
            {
                passed = true;
            }
            else
            {
                passed = false;
                info = new ExtensionRuleViolationInfo(this.ErrorMessage, context.Destination, context.ResponsePayload);
            }          

            return passed;
        }
    }
